package com.example.controlflow;

public class ExampleFor {
	
	public static void main(String[] args) {
		
		/* A for loop consists of 3 statements
		 * 1st statement is executed once
		 * 2nd is a condition for the for loop to finish
		 * 3rd statement is executed every loop
		 */
		
		
		for(int i = -10; i <= 10; i = i + 3 ) {
			// declare and initialize an int variable 
			// conditional statement 
			// increment step 
			System.out.println(i);
			
		}
		
	}

}
